#declaração de lista

lista = ['Maça', 'Uva', 'Laranja', 'Melão', 'Mamão', 'Morango', 'Manga']

# exibindo lista
print(lista)

#exibindo itens especificos da lista
print(lista[0]) #imprimindo o 1º item da lista
print(lista[2]) #imprimindo o 3º item da lista

# exibindo itens um abaixo do outro
for fruta in lista:
    print(fruta)
